package normal.mainui;

import java.awt.*;
import javax.swing.*;
import javax.swing.tree.*;
import normal.packetui.*;

/**
 * A renderer for drawing icons in a visual packet tree.
 */
public class PacketTreeCellRenderer extends DefaultTreeCellRenderer {
    /**
     * Create a new renderer.
     */
    public PacketTreeCellRenderer() {
    }

    /**
     * Return the component that will display the current tree node.
     */
    public Component getTreeCellRendererComponent(
            JTree tree,
            Object value,
            boolean sel,
            boolean expanded,
            boolean leaf,
            int row,
            boolean hasFocus) {
        super.getTreeCellRendererComponent(tree, value, sel, expanded,
            leaf, row, hasFocus);
        setIcon(PacketUIManager.packetIcon(((PacketTreeNode)value).
            getPacket()));
        return this;
    }
}
